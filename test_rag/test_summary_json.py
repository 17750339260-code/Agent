def test_json_longcase():
    headers = {"Content-Type": "application/json"}
    response = requests.post(URL, json=PAYLOAD, headers=headers)
    try:
        resp_json_longcase = response.json()
        print(json.dumps(resp_json_longcase, ensure_ascii=False, indent=2))
    except:
        print(response.text)
        pytest.fail("响应不是合法的 JSON")
    # 断言
    assert response.status_code == 200, f"期望状态码 200，实际 {response.status_code}"
    assert resp_json_longcase.get(
        "success") is True, f"success 字段应为 true，实际为 {resp_json_longcase.get('success')}"
    assert "data" in resp_json_longcase, "响应缺少 data 字段"
    assert isinstance(resp_json_longcase["data"], dict), "data 应为对象"


